package ex_010_For_LOOP;

public class LAB100_For_Loop_Basics_Float_Long {
    public static void main(String[] args) {
        for ( float f = 0.0f ; f < 10.67 ; f++) {
            System.out.println("Hi , Float -> " + f );
        }

        for ( long i = 1; i < 10; i++) {
            System.out.println(i);
        }
    }
}
