package ex_010_For_LOOP;

public class LAB095_for_loop_Basics {
    public static void main(String[] args) {
        // For Loop
        // Helps you to repeat the blocks
        // Initialization -> Condition -> Updation (Incre/Decre)
        // ICU

        for (int i = 0; i < 10 ; i++) {
            System.out.println(i);
            // 0,1,2....9 ->  times it got executed -> 10 times ( 0 - 9)
        }
    }
}
