package ex_003_java_Basics;

public class LAB031_Arithmetic_Operators {
    public static void main(String [] args) {
        // Arithmetic Operators
        // + Addition
        // - Subtraction
        // * Multiplication
        // / Division
        // % Modulo / Modulus - Remainder

        int a = 20;
        int b = 10;
        float c = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/c);
        System.out.println(a%b);

    }
}
