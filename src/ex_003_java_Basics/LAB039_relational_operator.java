package ex_003_java_Basics;

public class LAB039_relational_operator {
    public static void main(String[] args) {
        // == -> compare the values

        System.out.println( 10 == 10);
        System.out.println(10 < 10);
        System.out.println(10 == 20);
        System.out.println(10 <= 10);
        // 10 < 10 or 10 = 10

        System.out.println(10 >= 10);
    }
}