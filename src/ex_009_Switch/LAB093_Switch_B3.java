package ex_009_Switch;

public class LAB093_Switch_B3 {
    public static void main(String[] args) {

        int a = 11;
        switch (-1) {
            default :
                System.out.println("default");
                break;
            case -1 :
                System.out.println("10");
                break;
            case 9:
                System.out.println("9");

        }
    }
}
