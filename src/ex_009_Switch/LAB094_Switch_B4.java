package ex_009_Switch;

public class LAB094_Switch_B4 {
    public static void main(String[] args) {
        char code = 'C';
        switch (code) {
             default:
                System.out.println("Heyyy");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
        }
    }
}
