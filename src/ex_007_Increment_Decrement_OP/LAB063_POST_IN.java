package ex_007_Increment_Decrement_OP;

public class LAB063_POST_IN {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);


        // POST Increment -> print first and then increase
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);


    }
}
